export const LanguageCurrencyProvider = ({ children }) => {
  // Initialize with saved preferences or defaults
  const getInitialLanguage = () => {
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('language')
      if (savedLanguage && translations[savedLanguage]) {
        return savedLanguage
      }
    }
    return 'English'
  }

  const getInitialCurrency = () => {
    if (typeof window !== 'undefined') {
      const savedCurrency = localStorage.getItem('currency')
      if (savedCurrency && currencyRates[savedCurrency] !== undefined) {
        return savedCurrency
      }
    }
    return 'PKR - Pakistani Rupee'
  }

  const [language, setLanguage] = useState(getInitialLanguage)
  const [currency, setCurrency] = useState(getInitialCurrency)

  useEffect(() => {
    // Load saved preferences on mount
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('language')
      const savedCurrency = localStorage.getItem('currency')
      if (savedLanguage && translations[savedLanguage]) {
        setLanguage(savedLanguage)
      }
      if (savedCurrency && currencyRates[savedCurrency] !== undefined) {
        setCurrency(savedCurrency)
      }
    }
  }, [])

  const t = (key) => {
    return translations[language]?.[key] || translations['English']?.[key] || key
  }

  const formatCurrency = (amount) => {
    const rate = currencyRates[currency] || 1
    const convertedAmount = amount * rate
    const symbol = currencySymbols[currency] || '$'
    
    // Format number based on currency
    if (currency === 'PKR - Pakistani Rupee' || currency === 'INR - Indian Rupee') {
      return `${symbol} ${convertedAmount.toLocaleString('en-US', { minimumFractionDigits: 0, maximumFractionDigits: 0 })}`
    }
    return `${symbol}${convertedAmount.toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`
  }

  const getCurrencySymbol = () => {
    return currencySymbols[currency] || '$'
  }

  // Function to translate product names
  const translateProductName = (productName) => {
    // Map product names to translation keys
    const productNameMap = {
      'Modern table lamp': 'productModernTableLamp',
      'Smart speaker gray': 'productSmartSpeakerGray',
      'Smart watch white': 'productSmartWatchWhite',
      'Wireless headphones': 'productWirelessHeadphones',
      'Smart watch black': 'productSmartWatchBlack',
      'Security Camera': 'productSecurityCamera',
      'Smart Pen for iPad': 'productSmartPenForIpad',
      'Home Theater': 'productHomeTheater',
      'Apple Wireless Earbuds': 'productAppleWirelessEarbuds',
      'Apple Smart Watch': 'productAppleSmartWatch',
      'RGB Gaming Mouse': 'productRgbGamingMouse',
      'Smart Home Cleaner': 'productSmartHomeCleaner',
    }
    
    const translationKey = productNameMap[productName]
    if (translationKey) {
      return t(translationKey)
    }
    // If no translation found, return original name
    return productName
  }

  const updateLanguage = (newLanguage) => {
    setLanguage(newLanguage)
    localStorage.setItem('language', newLanguage)
  }

  const updateCurrency = (newCurrency) => {
    setCurrency(newCurrency)
    localStorage.setItem('currency', newCurrency)
  }

  return (
    <LanguageCurrencyContext.Provider
      value={{
        language,
        currency,
        t,
        formatCurrency,
        getCurrencySymbol,
        translateProductName,
        updateLanguage,
        updateCurrency,
      }}
    >
      {children}
    </LanguageCurrencyContext.Provider>
  )
}

